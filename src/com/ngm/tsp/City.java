package com.ngm.tsp;

public class City {
    private double cX = 0; //X-coordinate of a city
    private double cY = 0; //Y-coordinate of a city

    public double getcX() {
        return cX;
    }

    public void setcX(double xCoordinate) {
        this.cX = xCoordinate;
    }

    public double getcY() {
        return cY;
    }

    public void setcY(double yCoordinate) {
        this.cY = yCoordinate;
    }
}
